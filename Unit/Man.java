package java2.java2_oop.Unit;

public abstract class Man {

    protected float hp;
    protected int maxHp;
    protected int damage;
    protected int att;
    protected int def;

    public Man(float hp, int maxHp, int damage, int att, int def) {
        this.hp = hp;
        this.maxHp = maxHp;
        this.damage = damage;
        this.att = att;
        this.def = def;
    }

    
}    
    


